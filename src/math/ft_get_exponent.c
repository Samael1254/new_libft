int	ft_get_exponent(double nb)
{
	int	a;

	a = 0;
	if (nb == 0)
		return (0);
	if (nb < 0)
		nb *= -1;
	while (nb >= 10)
	{
		a++;
		nb = nb / 10;
	}
	while (nb < 1)
	{
		a--;
		nb = nb * 10;
	}
	return (a);
}
