int	ft_count_words(char *s, char sep)
{
	int	n;

	n = 0;
	while (*s)
	{
		while (*s == sep && *s)
			s++;
		if (*s)
			n++;
		while (*s != sep && *s)
			s++;
	}
	return (n);
}
